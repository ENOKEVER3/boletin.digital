  private void changeLabels() {
    if(teachersBox.getSelectedIndex() == 0) return;
    
    teacher = (User) teachers.get(teachersBox.getSelectedIndex() - 1);
    
    nameField.setText(teacher.getName());
    lastnameField.setText(teacher.getLastname());
  }

  private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
    if(!(JOptionPane.showConfirmDialog(null, "Est√° seguro?") == 0)) return;
    
    if(teachersBox.getSelectedIndex() == 0) {
      JOptionPane.showMessageDialog(null, "Indique el profesor");
    } else {
      manageSubject.teacherCod = User.getUserCodeByUsername(teachersBox.getSelectedItem().toString());
      try {
        manageSubject.addTeacher(manageSubject.teacherCod);
      } catch (ParseException ex) {
        Logger.getLogger(TeachersList.class.getName()).log(Level.SEVERE, null, ex);
      }
      manageSubject.setVisible(true);
      dispose();
    }
  }//GEN-LAST:event_jToggleButton1ActionPerformed

  private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
    teachers = User.getUsersFromCategories("Profesor"); 
   
    teachersBox.removeAllItems();
    teachersBox.addItem("");
    
    for(int i = 0; i < teachers.size(); i++) {
      User teacher = (User) teachers.get(i);
      teachersBox.addItem(teacher.getUsername());
    }
    
    changeLabels();
  }//GEN-LAST:event_formWindowActivated

